    public void refreshTable(){
        int rowCount = productList.getRowCount();
        DefaultTableModel model = (DefaultTableModel) productList.getModel();
        //dtm.setRowCount(0);
        for(int i=rowCount-1;i>=0;i--){
            model.removeRow(i);
        }
        for(Product p : supplier.getProductList().getProductList()){
            Object[] row = new Object [3];
            row[0] = p;
            row[1] = p.getModelNumber();
            row[2] = p.getPrice();
            
            model.addRow(row);
            
            
        }
    }


