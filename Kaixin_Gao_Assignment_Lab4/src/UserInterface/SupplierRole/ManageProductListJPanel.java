    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int row = productList.getSelectedRow();
        if(row>=0){
            
            Product p = (Product)productList.getValueAt(row, 0);
            ViewProductJPanel vpdjp = new ViewProductJPanel(userProcessContainer, p);
             userProcessContainer.add("ViewProductJPanel", vpdjp);
             CardLayout layout = (CardLayout) userProcessContainer.getLayout();
             layout.next(userProcessContainer);
            
            }
        else{JOptionPane.showMessageDialog(null, "please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);}
    }//GEN-LAST:event_btnViewActionPerformed

