    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        IntegerVerifier intVerify = new IntegerVerifier();
        StringVerifier stringVerify = new StringVerifier();
        DoubleVerifier doubleVerify=new DoubleVerifier();
     if (doubleVerify.verify(priceJTextField) && stringVerify.verify(drugJTextField) && stringVerify.verify(diseaseJTextField)) {
        Disease disease=new Disease();
        Drug drug=new Drug();
        String diseaseName=diseaseJTextField.getText();
        String drugName=drugJTextField.getText();
        disease.setName(diseaseName);
        drug.setDrugName(drugName);
        
        request.setDisease(disease);
        request.setDrug(drug);
         
        request.setStatus("Completed");
        request.setPrice(Integer.valueOf(priceJTextField.getText()));
        
        JOptionPane.showMessageDialog(null, "disease information send to the patient");
     }else {
            JOptionPane.showMessageDialog(null, "Wrong inputs"); 
        }  
        
    }//GEN-LAST:event_btnSubmitActionPerformed

