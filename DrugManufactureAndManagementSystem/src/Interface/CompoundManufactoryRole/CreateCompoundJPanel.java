    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
           Compound c=compoundHistory.addCompound();
        c.setName(nameJTextField.getText());
        c.setNum(Integer.parseInt(idJTextField.getText()));
        c.setDescription(descriptionJTextField.getText());
        c.setMolecularFormula(formulaJTextField.getText());
        c.setMolecularStructure(structureJTextField.getText());
        
         JOptionPane.showMessageDialog(null,"Compound Successfully Created");
    }//GEN-LAST:event_btnCreateActionPerformed

