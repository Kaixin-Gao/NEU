    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       IntegerVerifier intVerify = new IntegerVerifier();
        StringVerifier stringVerify = new StringVerifier();
        
        if (intVerify.verify(jTextField1) && stringVerify.verify(resultTextField)) {
         String result = resultTextField.getText();
         int funding=Integer.parseInt(jTextField1.getText());
        RequestForResearch request = new  RequestForResearch();
        request.setMessage(result);
        request.setFunding(funding);
        request.setSender(userAccount);
        request.setStatus("Sent");
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof ScientistOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
            
            JOptionPane.showMessageDialog(null, " The Study Topic Has Been Send To Scientists");
        }
        }else {
            JOptionPane.showMessageDialog(null, "wrong input");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

