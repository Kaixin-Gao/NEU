    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAccounts.getSelectedRow();
        if(selectedRow>=0){
            
            Account account = (Account)tblAccounts.getValueAt(selectedRow, 0);
            ViewAccountPanel panel = new ViewAccountPanel(userProcessContainer, account);
             userProcessContainer.add("ViewAccountPanel", panel);
             CardLayout layout = (CardLayout) userProcessContainer.getLayout();
             layout.next(userProcessContainer);
            
            }
        else{JOptionPane.showMessageDialog(null, "please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);}
        
    }//GEN-LAST:event_btnViewActionPerformed

