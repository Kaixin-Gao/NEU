    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAccounts.getSelectedRow();
        if(selectedRow>=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Delete the account details?", "Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){Account account = (Account) tblAccounts.getValueAt(selectedRow, 0);
            accountDirectory.deleteAccount(account);
            populateTable();
            }else{JOptionPane.showMessageDialog(null, "please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);}
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSreachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSreachActionPerformed
        // TODO add your handling code here:
        Account result = accountDirectory.searchAccount(txtAccountNumber.getText());
        if(result == null){JOptionPane.showMessageDialog(null, "Can not find shuch account", "Information", JOptionPane.INFORMATION_MESSAGE);}
        else{
            ViewAccountPanel panel = new ViewAccountPanel(userProcessContainer, result);
             userProcessContainer.add("ViewAccountPanel", panel);
             CardLayout layout = (CardLayout) userProcessContainer.getLayout();
             layout.next(userProcessContainer);
        
        
        }
    }//GEN-LAST:event_btnSreachActionPerformed


