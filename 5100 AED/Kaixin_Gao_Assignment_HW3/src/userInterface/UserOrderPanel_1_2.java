    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String payment = txtPayment.getText();
        String phone = txtPhone.getText();
        String item = txtItem.getText();
        String qty = txtQty.getText();
        
        if((!"".equals(username) && username.length() != 0) && (username.replaceAll("[\u4e00-\u9fa5]*[a-z]*[A-Z]* *", "").length()==0)){
            if((!"".equals(email) && email.length() != 0) && (email.replaceAll("[@]*[.]*[a-z]*[A-Z]*[0-9]*_*", "").length()==0)){
                if((!"".equals(phone) && phone.length() == 10) && (phone.replaceAll("[0-9]", "").length()==0)){
                    if((!"".equals(address) && address.length() != 0) && (address.replaceAll("#*[a-z]*[A-Z]*[0-9]* *-*", "").length()==0)){
                        if((!"".equals(payment) && payment.length() == 16) && (payment.replaceAll("[0-9]", "").length()==0)){
                            if((!"".equals(item) && item.length() != 0) && (item.replaceAll("[a-z]*[A-Z]*#*[0-9]*-*_*/* *", "").length()==0)){
                                if((!"".equals(qty) && qty.length() != 0) && (qty.length() <= 2) && (qty.replaceAll("[0-9]*", "").length()==0)){
                                    Order order = orderList.addOrder();
                                    order.setUsername(username);
                                    order.setEmail(email);
                                    order.setAddress(address);
                                    order.setPayment(payment);
                                    order.setPhone(phone);
                                    order.setItem(item);
                                    order.setQty(qty);
                                    JOptionPane.showMessageDialog(null, "Order Created!");
                                } else{JOptionPane.showMessageDialog(null,"FAILED! Please check your QTY.");}     
                           } else{JOptionPane.showMessageDialog(null,"FAILED! Please check your ITEM.");}         
                        } else{JOptionPane.showMessageDialog(null,"FAILED! Please check your VISA CARD.");}             
                    } else{JOptionPane.showMessageDialog(null,"FAILED! Please check your ADDRESS.");}                
                }else{JOptionPane.showMessageDialog(null,"FAILED! Please check your PHONE.");}       
            }else{JOptionPane.showMessageDialog(null,"FAILED! Please check your EMAIL.");}
        }else{JOptionPane.showMessageDialog(null,"FAILED! Please check your NAME.");}
        
        
    }//GEN-LAST:event_btnOrderActionPerformed

